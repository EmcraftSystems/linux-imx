#define __DTS_IMXRT10XX_PADCTRL_H

#define MXRT10XX_PAD_HYS		(1 << 16)

#define MXRT10XX_PAD_PUS(n)		((n & 0x3) << 14)
#define MXRT10XX_PAD_PUS_100K_PD	MXRT10XX_PAD_PUS(0)
#define MXRT10XX_PAD_PUS_47K_PU		MXRT10XX_PAD_PUS(1)
