#define MXRT10XX_PAD_DSE_25_OHM_1V8	MXRT10XX_PAD_DSE(6)
#define MXRT10XX_PAD_DSE_21_OHM_1V8	MXRT10XX_PAD_DSE(7)

#define MXRT10XX_PAD_DSE_260_OHM_3V3	MXRT10XX_PAD_DSE(1)
